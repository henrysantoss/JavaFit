    private void btnAddMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnAddMouseClicked
        if (edtAluno.getSelectedItem().toString().isEmpty()){
            JOptionPane.showMessageDialog(rootPane, "Por favor, selecione um aluno");
        }
        else if (edtData.getText().isEmpty()){
            JOptionPane.showMessageDialog(rootPane, "Por favor, selecione uma data");
        }
        else if (edtValor.getText().isEmpty()){
            JOptionPane.showMessageDialog(rootPane, "Valor vazio");
        }
        else if (edtData.isEditable() != true){
            JOptionPane.showMessageDialog(rootPane, "Não é possivel gerar uma fatura");
        }
        else {
            try{
                String sql_gerador = "select * from FATURA";
                String sql_inserir = "insert into FATURA values(?,?,?,?,?)";
                int gen=1;
                pst = con.prepareStatement(sql_gerador);
                rs = pst.executeQuery();
                if(rs.next()){
                    while(rs.next()){
                        gen = Integer.valueOf(rs.getString("id_fatura"));
                    }
                    gen++;
                }
                else {
                    gen = 1;
                }
                
                memo.setText("");
                memo.append("-------------Fatura---------------");
                memo.append("\n\nAluno: "+edtAluno.getSelectedItem().toString());
                memo.append("\nData do Pagamento: "+edtData.getText());
                memo.append("\nValor: "+edtValor.getText());
                memo.append("\n\nJAVAFIT: 2022-2022");
                
                pst = con.prepareStatement(sql_inserir);
                pst.setString(1, String.valueOf(gen));
                pst.setString(2, edtAluno.getSelectedItem().toString());
                pst.setString(3, edtData.getText());
                pst.setString(4, edtValor.getText());
                pst.setString(5, memo.getText());
                pst.executeUpdate();
            }
            catch(Exception e){
                
            }
        }
    }//GEN-LAST:event_btnAddMouseClicked

