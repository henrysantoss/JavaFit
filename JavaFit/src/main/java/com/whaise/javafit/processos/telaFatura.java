    public void lista_aluno(){
        String sql = "select * from aluno";
        edtAluno.removeAllItems();
        try{
            pst = con.prepareStatement(sql);
            rs = pst.executeQuery();
            while(rs.next()){
                edtAluno.addItem(rs.getString("ds_aluno"));
            }
        }
        catch(Exception e){
            
        }
    }
    
    public void busca_valor(){
        String sql = "select ds_valor from planos where ds_aluno = ?";
        edtValor.setText("");
        try{
            pst = con.prepareStatement(sql);
            pst.setString(1, edtAluno.getSelectedItem().toString());
            rs = pst.executeQuery();
            if(rs.next()){
                edtValor.setText(rs.getString("ds_valor"));
            }
        }
        catch(Exception e){
            
        }
    }
    
    public void verifica_fatura(){
        String sql = "select * from fatura where ds_aluno = ?";
        try{
            pst = con.prepareStatement(sql);
            pst.setString(1, edtAluno.getSelectedItem().toString());
            rs = pst.executeQuery();
            if(rs.next()){
                edtData.setText(rs.getString("ds_data"));
                edtData.setEditable(false);
                memo.setText(rs.getString("ds_fatura"));
                btnAdd.setEnabled(false);
            }
            else {
                edtData.setEditable(true);
                btnAdd.setEnabled(true);
            }
        }
        catch(Exception e){
            
        }
    }
    

