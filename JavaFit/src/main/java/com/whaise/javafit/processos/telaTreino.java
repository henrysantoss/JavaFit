    public void lista_treinos(){
        String sql_busca = "select * from treino";
        int gen =0;
        edtTreinoSalvo.removeAllItems();
        try {
            pst = con.prepareStatement(sql_busca);
            rs = pst.executeQuery();
                while(rs.next()){
                        gen++;
                    }
           }
           catch(Exception e){
               System.out.println("Erro: " + e.getMessage());
           }
        for (int i=0;i<gen;i++){
            edtTreinoSalvo.addItem("Treino "+String.valueOf(i+1));
        }
        
    }

