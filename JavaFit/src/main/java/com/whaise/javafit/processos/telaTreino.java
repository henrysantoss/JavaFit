    private void btnAddMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnAddMouseClicked
       String sql_select = "select * from TREINO";
       String sql_salvatreino = "insert into TREINO values(?,?)";
       String memoSalvar = "";
       int gen = 1;
       if (memo.getLineCount() <= 2) {
            JOptionPane.showMessageDialog(null, "Treino Vazio para Salvar");
       }
       else {
           try {
                pst = con.prepareStatement(sql_select);
                rs = pst.executeQuery();
                if(rs.next()){
                    while(rs.next()){
                        gen = Integer.valueOf(rs.getString("id_treino"));
                    }
                    gen ++;
                }
                else {
                    gen = 1; 
                }
                
                for (String linha : memo.getText().split(";")){
                  memoSalvar += linha+";";   
                }
                
                pst = con.prepareStatement(sql_salvatreino);
                pst.setString(1, String.valueOf(gen));
                pst.setString(2, memoSalvar);
                pst.executeUpdate();
                JOptionPane.showMessageDialog(null, "Treino Salvo!");
              
           }
           catch(Exception e){
               System.out.println("Erro: " + e.getMessage());
           }
           lista_treinos();
       }

    }//GEN-LAST:event_btnAddMouseClicked

