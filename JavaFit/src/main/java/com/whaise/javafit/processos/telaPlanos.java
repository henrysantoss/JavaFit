    private void btnAddMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnAddMouseClicked
        DateTimeFormatter dtf = DateTimeFormatter.ofPattern("HH:mm dd/MM/yyyy");

        if (edtModalidade.getSelectedItem().equals("Selecione")) {
            JOptionPane.showMessageDialog(rootPane, "Por favor, selecione a modalidade");
        } else if (edtPlano.getText().isEmpty() == true) {
            JOptionPane.showMessageDialog(rootPane, "Por favor, preencha o plano");
        } else if (edtValor.getText().isEmpty() == true) {
            JOptionPane.showMessageDialog(rootPane, "Por favor, preencha o valor mensal");
        } else {
            JOptionPane.showMessageDialog(rootPane, "Plano cadastrado com sucesso!\n" + dtf.format(LocalDateTime.now()));
        }

        try{
                String sql_gerador = "select * from PLANOS";
                String sql_inserir = "insert into PLANOS values(?,?,?,?,?)";
                float valor = 0;
                int gen=1;
                pst = con.prepareStatement(sql_gerador);
                rs = pst.executeQuery();
                if(rs.next()){
                    while(rs.next()){
                        gen = Integer.valueOf(rs.getString("id_plano"));
                    }
                    gen++;
                }
                else {
                    gen = 1;
                }
              
                
                pst = con.prepareStatement(sql_inserir);
                pst.setString(1, String.valueOf(gen));
                pst.setString(2, edtPlano.getText());
                pst.setString(3, edtAluno.getSelectedItem().toString());
                pst.setString(4, edtModalidade.getSelectedItem().toString());
                pst.setString(5, edtValor.getText());
                pst.executeUpdate();
                mostra_lista();
                edtPlano.setText("");
                edtValor.setText("");
                edtAluno.setSelectedIndex(-1);
                edtModalidade.setSelectedIndex(-1);
            }
            catch(Exception e){
                
            }
    }//GEN-LAST:event_btnAddMouseClicked

