    public ArrayList<GetPlanos> ListaPlanos(){
        ArrayList<GetPlanos> ListaPlanos = new ArrayList<>();
        String sql = "select * from planos";
        try{
            pst = con.prepareStatement(sql);
            rs = pst.executeQuery();
            GetPlanos plano;
            while(rs.next()){
                plano = new GetPlanos(rs.getString("id_plano"),rs.getString("ds_plano"),rs.getString("ds_aluno"),rs.getString("ds_modalidade"),rs.getString("ds_valor"));
                ListaPlanos.add(plano);
            }
        }
        catch(Exception e){
            
        }
        return ListaPlanos;
    }
    
    public void mostra_lista(){
        ArrayList<GetPlanos> lista = ListaPlanos();
        DefaultTableModel modelo = (DefaultTableModel)Tabela.getModel();
        Object[] campos = new Object[5];
        modelo.setRowCount(0);
        for(int i=0;i<lista.size();i++){
            campos[0] = lista.get(i).GetId();
            campos[1] = lista.get(i).GetNome();
            campos[2] = lista.get(i).GetAluno();
            campos[3] = lista.get(i).GetModalidade();
            campos[4] = lista.get(i).GetValor();
            modelo.addRow(campos);
        }
    }
    
